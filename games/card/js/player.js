// Player constructor.
function Player(playerType, health, mana, strength, agility, speed)
{
    // Type of player.
    this.playerType = playerType;

    // Health of player.
    this.health = health;

    // Mana of player.
    this.mana = mana;

    // Personal information of player.
    this.strength = strength;
    this.agility = agility;
    this.speed = speed;
}