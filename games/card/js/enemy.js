// Enemy constructor.
function Enemy(enemyType, health, mana, strength, agility, speed)
{
  // Type of enemy.
  this.enemyType = enemyType;

  // Health of enemy.
  this.health = health;

  // Mana of enemy.
  this.mana = mana;

  // Personal information of enemy.
  this.strength = strength;
  this.agility = agility;
  this.speed = speed;
}